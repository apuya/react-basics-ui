// Centralized component variant types

// Common sizes
export type ComponentSize = 'sm' | 'md' | 'lg';
export type ExtendedSize = 'xs' | 'sm' | 'md' | 'lg' | 'xl';

// Button variants
export type ButtonVariant = 'solid' | 'outline' | 'ghost' | 'link';

// Color/status variants
export type ColorVariant = 'default' | 'primary' | 'secondary' | 'success' | 'warning' | 'error' | 'info';
export type StatusVariant = 'success' | 'warning' | 'error' | 'info';

// Common style variants
export type StyleVariant = 'solid' | 'outline' | 'subtle' | 'ghost';

// Input/form control sizes
export type FormControlSize = 'sm' | 'md' | 'lg';

// Orientation
export type Orientation = 'horizontal' | 'vertical';

// Alignment
export type Alignment = 'start' | 'center' | 'end';
export type JustifyContent = 'start' | 'center' | 'end' | 'between' | 'around' | 'evenly';

// Position
export type Position = 'top' | 'right' | 'bottom' | 'left';
export type Side = 'top' | 'right' | 'bottom' | 'left';
